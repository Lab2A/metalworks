class ReportSummary(BaseModel):
    """Compact shape for list endpoints and artifact cards."""

    report_id: str
    query: str
    fork: Fork
    total_threads: int
    total_distinct_authors: int
    generated_at: datetime
    top_claims: list[str] = Field(default_factory=list)
    brief_id: str | None = None

    @classmethod
    def from_report(cls, report: DemandReport, top_n: int = 3) -> ReportSummary:
        return cls(
            report_id=report.report_id,
            query=report.query,
            fork=report.fork,
            total_threads=report.total_threads,
            total_distinct_authors=report.total_distinct_authors,
            generated_at=report.generated_at,
            top_claims=[c.claim for c in report.ranked_clusters[:top_n]],
            brief_id=report.brief.brief_id if report.brief else None,
        )


