class TriageThresholds(BaseModel):
    """Three-bucket cutoffs for the exploration step.

    Top auto_accept_pct: high cosine to question → skip LLM, accept.
    Bottom auto_reject_pct: low cosine → skip LLM, reject.
    Middle: LLM classifier with confidence.

    Gate on absolute cosine bands AND percentile. Both cosine_floor /
    cosine_ceiling and the percentages are honored; if they disagree by >5%
    relevance rate, the triage step alerts.
    """

    auto_accept_pct: float = Field(default=0.20, ge=0.0, le=1.0)
    auto_reject_pct: float = Field(default=0.50, ge=0.0, le=1.0)
    cosine_floor: float | None = Field(
        default=None,
        description="Absolute cosine below which to auto-reject regardless of percentile. "
        "None = percentile-only.",
    )
    cosine_ceiling: float | None = Field(
        default=None,
        description="Absolute cosine above which to auto-accept regardless of percentile. "
        "None = percentile-only.",
    )


