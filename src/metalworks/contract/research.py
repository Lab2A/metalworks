class CrossReference(BaseModel):
    """One verified link between a Reddit cluster and the web findings.

    At the LLM-output boundary, the triangulator returns list-id-prefixed
    indices ('cluster:3', 'web:7'). The triangulator validator strips the
    prefix and enforces that cluster ids are valid InsightCluster ranks and
    web ids are valid WebFinding finding_indexes before producing this
    contract. Mixed-list refs are rejected.
    """

    cluster_rank: int = Field(description="1-based rank, references InsightCluster.rank.")
    web_finding_indices: list[int] = Field(
        description="1-based indices, reference WebFinding.finding_index."
    )
    agreement: Literal["agree", "silent_web", "silent_corpus", "disagree"] = Field(
        description="How the streams relate on this claim."
    )
    note: str = Field(
        description="One-line synthesis: where they converge, or what the disagreement is."
    )


