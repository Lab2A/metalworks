class SlotPlan(BaseModel):
    product: str | None = Field(default=None)
    audience: str | None = Field(default=None)
    price: float | None = Field(default=None)
    given: list[str] = Field(default_factory=list)
    find: list[str] = Field(default_factory=list)

    @classmethod
    def resolve(
        cls,
        *,
        product: str | None = None,
        audience: str | None = None,
        price: float | None = None,
    ) -> SlotPlan:
        vals: dict[str, object] = {"product": product, "audience": audience, "price": price}

        def is_given(name: str, v: object) -> bool:
            if name == "price":
                return isinstance(v, int | float) and not isinstance(v, bool) and math.isfinite(v)
            return isinstance(v, str) and v.strip() != ""

        given = [n for n, v in vals.items() if is_given(n, v)]
        if not given:
            raise ValueError(
                "At least one slot (product, audience, or price) must be given as the seed."
            )
        find = [n for n in ("product", "audience", "price") if n not in given]
        return cls(product=product, audience=audience, price=price, given=given, find=find)


