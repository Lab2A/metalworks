class DemandReport(BaseModel):
    """The full report — the canonical shape returned by every surface.

    Optional / default-empty fields keep the additive-contract parity guard
    passing (reports produced by earlier shapes validate against this).
    """

    # ── Core ──
    report_id: str
    client_id: str = Field(
        default="local",
        description="Owning tenant. Set by the service, never the LLM. "
        "Defaults to 'local' for library/CLI use.",
    )
    # ── Lineage (live-view versioning) ──
    # A report is a VIEW over the corpus; a `research refresh` re-synthesizes
    # against the now-larger corpus and pins a NEW version in the same lineage.
    # The materialized artifact stays keyed by `report_id` (unique per version,
    # so `runs/<report_id>/` and portability are unchanged); `lineage_id` is the
    # stable handle that survives refreshes. Additive + defaulted: a v1 run and
    # any pre-lineage report both read as version 1 of their own lineage.
    lineage_id: str = Field(
        default="",
        description="Stable id for this report's refresh lineage. Empty ⇒ first "
        "version (read `effective_lineage_id`, which falls back to `report_id`).",
    )
    version: int = Field(
        default=1, description="Monotonic version within the lineage (1 = original run)."
    )
    parent_report_id: str | None = Field(
        default=None,
        description="The prior version's `report_id` this refresh supersedes. None for v1.",
    )
    query: str
    fork: Fork
    pinned_axis: str
    optimized_axis: str
    source: str = Field(
        default="reddit_arctic_shift",
        description="Provenance of the corpus this run drew on: "
        "'reddit_arctic_shift' | 'reddit_live' | 'hackernews' | 'reviews' | 'mixed'.",
    )
    date_range_start: datetime
    date_range_end: datetime
    total_threads: int
    total_distinct_authors: int
    ranked_clusters: list[InsightCluster]
    partial: bool = False
    caveat: str | None = None
    created_by: str = Field(
        default="library", description="'library' | 'cli' | 'mcp' | 'ui' | 'agent'."
    )
    generated_at: datetime

    # ── Verdict / sizing / pricing (all optional) ──
    verdict: str | None = None
    slot_plan: SlotPlan | None = None
    audience_profile: AudienceProfile | None = None
    segments: list[AudienceSegment] = Field(default_factory=list[AudienceSegment])
    market_sizing: MarketSizing | None = None
    price_finding: PriceFinding | None = None
    source_map: list[SourceMapEntry] = Field(default_factory=list[SourceMapEntry])

    # ── Two-stream additions ──
    brief: ResearchBrief | None = Field(
        default=None,
        description="The brief this run was produced against (frozen-version FK).",
    )
    web_findings: list[WebFinding] = Field(default_factory=list[WebFinding])
    corpus_stats: CorpusStats | None = None
    corpus_shape: ExplorationReport | None = None
    cross_references: list[CrossReference] = Field(default_factory=list[CrossReference])
    must_address_resolution: dict[str, str] = Field(
        default_factory=dict,
        description="must_address item → 'cluster:N' | 'web:N' | 'unaddressable: <reason>'.",
    )

    @property
    def effective_lineage_id(self) -> str:
        """The lineage handle, falling back to ``report_id`` for v1 / pre-lineage
        reports (where ``lineage_id`` is empty). Use this — never raw
        ``lineage_id`` — when grouping versions of the same report."""
        return self.lineage_id or self.report_id

    @property
    def evidence(self) -> list[EvidenceRecord]:
        """Flat, de-duplicated evidence backing this report.

        This is what downstream pillars' ``EvidenceRef``s resolve against
        (by ``EvidenceRecord.id``). Derived on access — never stored or
        serialized, so it is always consistent with the report's own fields
        after a JSON round-trip. De-dups by id (the same quote can back more
        than one cluster).
        """
        records: dict[str, EvidenceRecord] = {}
        for cluster in self.ranked_clusters:
            for q in cluster.quotes:
                records.setdefault(
                    q.id,
                    EvidenceRecord(
                        id=q.id, kind="quote", text=q.text, url=q.source_url, provenance="verbatim"
                    ),
                )
        for w in self.web_findings:
            records.setdefault(
                w.id,
                EvidenceRecord(
                    id=w.id, kind="web", text=w.claim, url=w.source_url, provenance="grounded-web"
                ),
            )
        if self.price_finding is not None:
            for p in self.price_finding.evidence:
                records.setdefault(
                    p.id,
                    EvidenceRecord(
                        id=p.id,
                        kind="price",
                        text=p.text,
                        url=p.permalink or "",
                        provenance="verbatim" if p.permalink else "derived",
                    ),
                )
        return list(records.values())


