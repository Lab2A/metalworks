"""Research data contract — single source of truth.

Field names are stable so downstream consumers can depend on them directly.

Every surface imports its shape from here:

    research pipeline ──┐
    CLI / REST          ├─► DemandReport (this module)
    MCP tools ──────────┘

TypeScript twins are generated by scripts/gen_ts_types.py — never
hand-maintained. JSON-schema snapshots in contract/schema/ are diff-gated.

Structural-provenance contract:
- The LLM proposes clusters + candidate quotes + web findings + cross-
  references; the service verifies quotes (exact-match), validates indices
  (list-id-prefixed at the LLM boundary, see metalworks.research.triangulate),
  and assembles THIS.
- Quote text is exact-matched to a stored source comment — no fabrication.
  Citations are the source-neutral `ResolvedCitation` (materialized inline so a
  report stays self-contained over git + MCP); `CitationRef` is the thin
  live-view pointer used where the corpus is present.
- Web findings carry URL + title + published_at from the grounding tool's
  citation metadata, not from the LLM's prose.
- Cross-references are indices-to-indices, never free text.
- Counts (threads_relevant, mention_count, distinct_author_count,
  must_address_resolution) are deterministic from membership.

Brief immutability: briefs are UUID-per-version. Editing a brief creates a
new row with a fresh brief_id and a `supersedes` pointer to the prior
version. Runs FK to a single brief_id; a re-run after edit creates a new run
pointing at the new brief.

Multi-tenancy: `workspace_id` / `client_id` are preserved for hosted
deployments but default to "local" — a library user never has to think
about tenants.
"""

