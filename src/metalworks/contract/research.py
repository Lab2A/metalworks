class ExplorationReport(BaseModel):
    """What the three-bucket triage actually produced for this run.

    Surfaced in the report's corpus_shape banner. The honest funnel:
    threads_pulled (raw archive) → threads_auto_{accepted,rejected} by
    cosine triage → threads_classified by middle-bucket LLM →
    threads_relevant (passed classifier) → threads_synthesized (post
    embed-group dedup).

    `noise_composition` is binned reason counts from the classifier — what
    KIND of irrelevant content dominated the rejected pile.
    """

    threads_pulled: int
    threads_auto_accepted: int
    threads_auto_rejected: int
    threads_classified: int
    threads_relevant: int
    threads_synthesized: int
    noise_composition: dict[str, int] = Field(default_factory=dict)
    similarity_percentiles: dict[str, float] = Field(default_factory=dict)
    hybrid_percentiles: dict[str, float] = Field(
        default_factory=dict,
        description=(
            "Percentile bands (p10..p90) of the blended cosine + BM25 hybrid "
            "score that actually drives the bucketing. Empty for legacy "
            "reports that ran on cosine-only triage."
        ),
    )


class CorpusStats(BaseModel):
    """Statistical aggregates across the relevant corpus.

    Distinct from ExplorationReport (which is the funnel) — these are
    distributions over the threads that survived triage and were synthesized.
    """

    percentile_bands: dict[str, int] = Field(
        default_factory=dict, description="Decile → thread count."
    )
    subreddit_distribution: list[SourceMapEntry] = Field(default_factory=list[SourceMapEntry])
    time_distribution: dict[str, int] = Field(
        default_factory=dict, description="ISO month → thread count."
    )


