class WebFinding(BaseModel):
    """One structured finding from the web research stream.

    Indexed by `finding_index` (1-based). The triangulator references
    findings by index — never by free text — preserving the structural-
    provenance contract across both streams.

    `source_url` / `source_title` / `published_at` come from the grounding
    tool's citation metadata, not from the LLM. The LLM produces only
    `claim` + `specifics` + index assignments.
    """

    finding_index: int = Field(description="1-based index within this report's web_findings list.")
    claim: str = Field(description="One-line factual claim from the web.")
    specifics: str = Field(description="The number, date, name, or quote that anchors the claim.")
    source_url: str = Field(description="From the grounding tool's citation metadata.")
    source_title: str = Field(description="From the grounding tool's citation metadata.")
    published_at: datetime | None = Field(
        default=None, description="From the grounding metadata when available."
    )
    confidence: SignalStrength = Field(description="Service-assigned, not LLM-assigned.")

    @computed_field
    @property
    def id(self) -> str:
        """Stable content-addressed evidence id (``w:<hash of source_url|claim>``)."""
        return _evidence_id("w", self.source_url, self.claim)


